// declare the setup function
function setup() {

    // create a canvas 500px wide and 500px high
    createCanvas( 500, 500 );

    // color the background 'grey'
    background( 'grey' );
}


// declare draw() function
function draw(){

  // using the text function, have p5 write, "Hello World!"
  // this will be positioned at ( x: 100, y: 100 ).
  text("Hello World!", 100, 100);
}
